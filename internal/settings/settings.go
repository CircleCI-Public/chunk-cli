package settings

import (
	"encoding/json"
	"fmt"

	"github.com/CircleCI-Public/chunk-cli/internal/config"
)

// HookEntry is one hook command within a hook group.
type HookEntry struct {
	Type    string `json:"type"`
	Command string `json:"command"`
	Timeout int    `json:"timeout"`
}

// HookGroup is a group of hooks filtered by matcher.
type HookGroup struct {
	Matcher string      `json:"matcher"`
	Hooks   []HookEntry `json:"hooks"`
}

// SettingsJSON is the .claude/settings.json structure.
type SettingsJSON struct {
	Schema      string                 `json:"$schema"`
	Comment     string                 `json:"_comment"`
	Permissions map[string][]string    `json:"permissions"`
	Hooks       map[string][]HookGroup `json:"hooks,omitempty"`
}

// Build generates .claude/settings.json content from commands.
// It creates a single PreToolUse hook matching "Bash(git commit*)" that
// runs each command directly.
func Build(commands []config.Command) ([]byte, error) {
	hooks := make([]HookEntry, 0, len(commands))
	for _, cmd := range commands {
		timeout := cmd.Timeout
		if timeout == 0 {
			timeout = 60
		}
		hooks = append(hooks, HookEntry{
			Type:    "command",
			Command: fmt.Sprintf("cd ${CLAUDE_PROJECT_DIR:-.} && %s", cmd.Run),
			Timeout: timeout,
		})
	}

	s := SettingsJSON{
		Schema:  "https://json.schemastore.org/claude-code-settings.json",
		Comment: "Generated by chunk init.",
		Permissions: map[string][]string{
			"allow": {"Bash(chunk:*)"},
		},
	}

	if len(hooks) > 0 {
		s.Hooks = map[string][]HookGroup{
			"PreToolUse": {
				{
					Matcher: CommitMatcher,
					Hooks:   hooks,
				},
			},
		}
	}

	return json.MarshalIndent(s, "", "  ")
}
